			<title>Как решать гармонические задачи?</title>
			<link>http://www.lafamire.ru/index.php?option=com_content&amp;view=article&amp;id=346:2010-03-16-20-43-26&amp;catid=10:2010-03-16-20-46-53&amp;Itemid=14</link>
			<guid>http://www.lafamire.ru/index.php?option=com_content&amp;view=article&amp;id=346:2010-03-16-20-43-26&amp;catid=10:2010-03-16-20-46-53&amp;Itemid=14</guid>
			<description><![CDATA[<p style="text-align: justify;"><span style="font-size: 14pt;"><span class="fs1">Как правильно решать гармонические задачи</span></span><span style="font-size: 14pt;"></span></p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;"><br /> Конечной и самой важной целью решения задач является такой уровень развития гармонического слуха, при котором учащийся может не только мысленно представлять звучание гармонизованной задачи, но и "сочинять музыкальные произведения", пользуясь пройденными в курсе гармонии аккордами, типовыми гармоническими оборотами  и, конечно же, писать 4-хголосные диктанты.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Как добиться высокого уровня развития слуха? Очень просто. Научиться мысленно представлять звучание музыки.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Подбирайте на фортепиано как можно больше музыки.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Попробуйте представить звучание мелодии, которая вам нравится.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Сыграйте несколько раз мелодию задачи, проверьте, знаете ли ее наизусть, спойте ее.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Попробуйте представить, как звучит мелодия, если бы вы играли ее на фортепиано, как она звучит, если бы вы ее пели.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Не получилось? Ничего страшного. Вы же делаете это в первый раз. Сыграйте или спойте мелодию столько раз, сколько нужно, чтобы вы "заболели" этой мелодией, чтобы она прочно "засела " в вашей голове. Решайте задачу так, как если бы вы подбирали аккомпанемент. Постарайтесь мыслить (слышать) функциями аккордов ( тоника, доминанта, субдоминанта), а также представлять и  звучание конкретных нот аккорда. Естественно, для записи решения задачи вы будете пользоваться гармоническими правилами, такими как определение формы задачи, определение и расстановка цезур, каденций, правильное удвоение тонов в аккордах, соединение аккордов, логика гармонического развития в ладу, будете следить за красотой и логичностью линии баса, проверять пары голосов на наличие параллельных квинт и октав, а также следить за множеством других нюансов, связанных с темой задачи.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Самым последним и самым важным действием в решении задачи должно быть ее проигрывание на фортепиано в правильном ритме и в подходящем для жанра задачи темпе - здесь вы услышите, хорошо ли звучит решенная таким образом задача, не нужны ли другие варианты ее гармонизации. При этом велика вероятность ошибок в игре, значит, вы так и не определите, все ли звучит хорошо. Поэтому мой вам совет - решайте задачу в музыкальном редакторе. Нажав кнопку "проигрывание", вы услышите все сильные и слабые стороны вашей задачи.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Вот советы, как научиться представлять звучание всей задачи.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">1) Научитесь представлять мелодию задачи (об этом уже говорилось)</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">2)Научитесь представлять линию баса</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">3)Пропойте или сыграйте каждый аккорд вашей задачи снизу вверх</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">4)Cыграйте первый аккорд задачи снизу вверх очень быстро, как бы арпеджиато, опору делайте на бас и мелодию</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">5)Представляйте этот аккорд мысленно так же быстро</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">6)Сыграйте все звуки аккорда одновременно</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">7)Представьте мысленно его звучание. Получилось?</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">8)Сделайте те же упражнения со вторым аккордом</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">9)Сыграйте оба аккорда снизу вверх один за другим сначала медленно, мысленно представьте, сыграйте арпеджиато, мысленно представьте, сыграйте оба аккорда с одновременным звучанием звуков каждого, мысленно представьте.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Если у вас все получилось, мои поздравления, но вы должны научиться представлять звучание всей задачи.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">10)Играйте и представляйте первые 4-5 аккордов задачи медленно, арпеджиато и, наконец, в гармоническом звучании (4-хзвучными аккордами).</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">11)Разбейте всю задачу на несколько отрывков по 4-5 аккордов в каждом. Научитесь играть и представлять любой из  отрывков.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">12)Проверьте, как вы знаете звучание отрывков, попытавшись представить их звучание не по порядку.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">13)Сыграйте всю задачу от начала и до конца. Попробуйте представить ее звучание.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">14)Не оставляйте попыток до тех пор, пока вы не будете знать в ней всех нот и пока не сможете ясно представить звучание задачи.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Первое время вы будете тратить на подобные упражнения примерно полтора-два часа, однако оно того стоит. Ваш музыкальный слух будет заметно отличаться от слуха ваших одногруппников остротой и тонкостью.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">15)Если вы ездите в транспорте или у вас есть любое незанятое время, вы можете вспоминать звучание задачи или любого музыкального произведения, которое вы выучили, каждый день. В этом случае ваш слух всегда будет активным и способным к сочинению музыки или к написанию музыкального диктанта.</p>
<div style="text-align: justify;"></div>
<p style="text-align: justify;">Желаю вам больших успехов в овладении профессией музыканта.</p>
Гамаюн.]]></description>
